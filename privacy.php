    <h1>Data Privacy Policy</h1>
    <p><strong>eSerbisyo: Barangay Service and Records Management System</strong></p>
    <p><em>Compliant with the Data Privacy Act of 2012 (RA 10173)</em></p>

    <h2>1. Information We Collect</h2>
    <p><strong>Personal Information:</strong></p>
    <ul>
      <li>Full name, address, date of birth</li>
      <li>Contact details (email, mobile number)</li>
      <li>Government-issued ID (for document requests)</li>
    </ul>
    <p><strong>Transaction Information:</strong></p>
    <ul>
      <li>Documents requested (e.g., clearance, certificate of residency)</li>
      <li>Request status and history</li>
      <li>Supporting documents uploaded</li>
    </ul>
    <p><strong>Technical Information:</strong></p>
    <ul>
      <li>IP address, browser type, device information</li>
      <li>System usage logs</li>
    </ul>

    <h2>2. How We Use Your Information</h2>
    <p>We use your information to:</p>
    <ul>
      <li>Process and fulfill document requests</li>
      <li>Verify your identity and residency</li>
      <li>Send notifications about request status</li>
      <li>Improve system performance and user experience</li>
      <li>Comply with legal and reporting requirements</li>
    </ul>

    <h2>3. Data Sharing</h2>
    <p>We DO NOT sell your personal information.</p>
    <p>We MAY share information with:</p>
    <ul>
      <li><strong>Barangay personnel</strong> – only those authorized to process your request</li>
      <li><strong>Government agencies</strong> – when required by law (e.g., DILG reporting)</li>
      <li><strong>Service providers</strong> – for system hosting and maintenance (bound by confidentiality)</li>
    </ul>

    <h2>4. Data Security</h2>
    <p>We implement appropriate security measures including:</p>
    <ul>
      <li>SSL encryption for data transmission</li>
      <li>Role-based access controls</li>
      <li>Firewalls and regular backups</li>
      <li>Audit logs to monitor system activity</li>
    </ul>

    <h2>5. Your Rights</h2>
    <p>Under the Data Privacy Act, you have the right to:</p>
    <ul>
      <li><strong>Access</strong> your personal information</li>
      <li><strong>Correct</strong> inaccurate data</li>
      <li><strong>Object</strong> to processing of your data</li>
      <li><strong>Request deletion</strong> of your data (subject to legal retention requirements)</li>
    </ul>
    <p>To exercise these rights, contact the Barangay Data Protection Officer.</p>

    <h2>6. Data Retention</h2>
    <p>We retain your information only as long as necessary:</p>
    <ul>
      <li>Transaction records: 5 years</li>
      <li>Resident profiles: Until account deletion or 10 years of inactivity</li>
      <li>Audit logs: 3 years</li>
    </ul>

